'use client';

import type { Metadata } from 'next';
import { Geist, Geist_Mono } from 'next/font/google';

import { CssBaseline } from '@mui/material';

import { MUIProvider } from '../components/MUIProvider';

import './globals.css';

const geistSans = Geist({
  variable: '--font-geist-sans',
  subsets: ['latin'],
});

const geistMono = Geist_Mono({
  variable: '--font-geist-mono',
  subsets: ['latin'],
});

// eslint-disable-next-line @typescript-eslint/no-unused-vars
const metadata: Metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
};

/**
 * The root layout of the application.
 *
 * This component wraps the entire application and handles rendering the
 * {@link https://mui.com/material-ui/react-css-baseline/ | MUI CSS baseline}.
 *
 * @param {{ children: React.ReactNode }} props The props of the component.
 * @returns {JSX.Element} The root layout of the application.
 */
export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  return (
    <html lang="en">
      <body className={`${geistSans.variable} ${geistMono.variable} antialiased`}>
        <MUIProvider>
          <CssBaseline />
          <main>{children}</main>
        </MUIProvider>
      </body>
    </html>
  );
}
